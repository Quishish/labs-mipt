y1 = [302300/10**9, 1956700/10**9, 4283000/10**9, 6396100/10**9, 11988100/10**9, 33052200/10**9, 40430200/10**9, 52269500/10**9, 84597800/10**9, 144492100/10**9]


# Кривая 2: квик  
y2 = [60800/10**9, 327600/10**9, 676000/10**9, 962500/10**9, 1693000/10**9, 5823100/10**9, 5677000/10**9, 7616700/10**9, 11488500/10**9, 19291700/10**9]


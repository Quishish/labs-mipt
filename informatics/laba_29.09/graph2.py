y3 = [327200/10**9, 1894400/10**9, 4213600/10**9, 6462000/10**9, 11146800/10**9, 32690300/10**9, 46990000/10**9, 50298500/10**9, 77484400/10**9, 141599100/10**9]


# Создаем график
plt.figure(figsize=(10, 6))

# Рисуем три кривые с разными стилями
plt.plot(x_values, y1, 'blue', linewidth=2, marker='o', markersize=6, label='comb sort')

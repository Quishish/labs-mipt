import matplotlib.pyplot as plt

# Данные для оси X
x_values = [1000, 5000, 10000, 15000, 25000, 50000, 75000, 100000]

# Три различных сценария времени выполнения (в секундах)
# Кривая 1: сортед
y1 = [3981700/10**9, 100068400/10**9, 397420900/10**9, 893490400/10**9, 2494040900/10**9, 9929017400/10**9, 22375240600/10**9, 40076822200/10**9]

# Кривая 2: ансортед  
y2 = [8581400/10**9, 214194600/10**9, 853420600/10**9, 1929938800/10**9, 5357551200/10**9, 21447955700/10**9, 48291306600/10**9, 85716777800/10**9]

# Кривая 3: рандом
y3 = [7080100/10**9, 183324300/10**9, 744009900/10**9, 1651468300/10**9, 4635438200/10**9, 18656309900/10**9, 41861062200/10**9, 75041574000/10**9]

# Создаем график
plt.figure(figsize=(10, 6))

# Рисуем три кривые с разными стилями
plt.plot(x_values, y1, 'blue', linewidth=2, marker='o', markersize=6, label='sorted')
plt.plot(x_values, y2, 'red', linewidth=2, marker='s', markersize=6, linestyle='--', label='unsorted')
plt.plot(x_values, y3, 'green', linewidth=2, marker='^', markersize=6, linestyle='-.', label='random')

# Настраиваем оси и заголовок
plt.xlabel('Количество элементов массива', fontsize=12)
plt.ylabel('Время (секунды)', fontsize=12)
plt.title('Время выполнения алгоритмов в зависимости от размера данных', fontsize=14)

# Устанавливаем пределы оси Y
plt.ylim(-0.05, 90)

# Настраиваем сетку
plt.grid(True, linestyle='--', alpha=0.7)

# Добавляем легенду
plt.legend(loc='upper left', fontsize=10)

# Настраиваем метки на оси X

plt.xticks([0, 50000, 100000])

plt.tight_layout()
plt.show()
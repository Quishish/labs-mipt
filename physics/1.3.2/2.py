import numpy as np

# Ваши данные
x = np.array([0.0275, 0.055, 0.0825, 0.11, 0.1375, 0.165, 0.22, 0.275])
y = np.array([0.0, 0.011929, 0.02662, 0.041191, 0.052775, 0.067418, 0.09703, 0.124383])

# Формируем матрицу A и вектор b
A = np.vstack([x, np.ones(len(x))]).T
b = y

# Решаем систему уравнений Ax = b методом наименьших квадратов
coefficients = np.linalg.lstsq(A, b, rcond=None)[0]

# Полученные коэффициенты линейной модели y = a * x + b
a, b = coefficients

print(f"Линейная модель: y = {a:.6f} * x + {b:.6f}")

# Для проверки, можно вычислить предсказанные значения y и сравнить с исходными
y_pred = a * x + b
print("Исходные значения y:", y)
print("Предсказанные значения y:", y_pred)